import mongoose, { Schema, models } from "mongoose"

const TweetSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    body: {
      type: String,
      required: true,
      trim: true,
    },
    user: {
      type: Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  {
    timestamps: true,
  }
)

const Tweet = models.Tweet || mongoose.model("Tweet", TweetSchema)

export default Tweet